#include <unistd.h>

void	print_numbers(int i, int j)
{
	char	c[5];

	c[2] = ' ';
	c[0] = '0';
	c[3] = '0';
	if (i >= 10)
	{
		c[0] = i / 10 + '0';
	}
	if (j >= 10)
	{
		c[3] = j / 10 + '0';
	}
	c[1] = i % 10 + '0';
	c[4] = j % 10 + '0';
	write(1, &c, 5);
	if (i != 98 || j != 99)
	{
		write(1, ", ", 2);
	}
}

void	ft_print_comb2(void)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (i < 99)
	{
		j = i + 1;
		while (j < 100)
		{
			print_numbers(i, j);
			j ++;
		}
		i ++;
	}
}
